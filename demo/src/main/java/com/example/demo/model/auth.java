package com.example.demo.model;


public class auth {

    private int serno;
    private int level;
    private String name;
    private String descr;

	public int getSerno() {
		return this.serno;
	}

	public void setSerno(int serno) {
		this.serno = serno;
	}

	public int getLevel() {
		return this.level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescr() {
		return this.descr;
	}

	public void setDescr(String descr) {
		this.descr = descr;
	}


    

}
